const fs = require('fs');
const file = './src/data/questions.ts';
let content = fs.readFileSync(file, 'utf8');

const categoriesRegex = /export const gameCategories: Category\[\] = (\[[\s\S]*?\]);\n/;
const boardRegex = /export const gameBoard: BoardCell\[\] = (\[[\s\S]*?\]);\n/m;

const catMatch = content.match(categoriesRegex);
const boardMatch = content.match(boardRegex);

if (catMatch && boardMatch) {
    let categories = eval(catMatch[1]);
    let board = eval(boardMatch[1]);

    newQuestions.forEach(q => {
        let categoryId = "";
        let existingCategory = categories.find(c => c.name === q.category);
        if (existingCategory) {
            categoryId = existingCategory.id;
        } else {
            categoryId = "cat" + (categories.length + 1);
            categories.push({ id: categoryId, name: q.category });
        }

        // Setup correctly for board cell
        q.correctAnswerIndex = q.options.indexOf(q.correctAnswer);

        let cell = board.find(c => c.categoryId === categoryId && c.points === q.value);
        if (cell) {
            cell.questions.push(q);
        } else {
            board.push({
                categoryId: categoryId,
                categoryName: q.category,
                points: q.value,
                questions: [q]
            });
        }
    });

    content = content.replace(catMatch[1], JSON.stringify(categories, null, 4));
    content = content.replace(boardMatch[1], JSON.stringify(board, null, 4));
    
    fs.writeFileSync(file, content);
    console.log("Appended new questions successfully.");
} else {
    console.log("Could not find categories or board.");
}
